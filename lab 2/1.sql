DDL:
DDL is Data Definition Language which is used to define data structures. For example: create table, 
alter table are instructions in SQL.
DML:
DML is Data Manipulation Language which is used to manipulate data itself. For example: insert, 
update, delete are instructions in SQL.
Difference between DDL and DML:
DDL
It stands for Data Definition Language.
It is used to create database schema and can be used to define some constraints as well.
It basically defines the column (Attributes) of the table.
It doesn’t have any further classification.
DDL does not use WHERE clause in its statement.
DML
It stands for Data Manipulation Language.
It is used to add, retrieve or update the data.
It add or update the row of the table. These rows are called as tuple.
It is further classified into Procedural and Non-Procedural DML.
While DML uses WHERE clause in its statement.
a. at least 3 DDL commands;
INSERT — вставляет в таблицу одну или несколько новых строк.
UPDATE — обновляет в одной или нескольких существующих строках таблицы значения 
одного или нескольких столбцов.
DELETE — удаляет из таблицы одну или несколько строк.
b. at least 4 DML commands.
• SELECT – выборка данных
• INSERT – вставка новых данных
• UPDATE – обновление данных
• DELETE – удаление данных
• MERGE – слияние данных
